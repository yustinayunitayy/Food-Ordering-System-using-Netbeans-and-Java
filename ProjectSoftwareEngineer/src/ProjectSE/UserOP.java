    private void UOPAddButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_UOPAddButtonMouseClicked
    String menuName = UOPMenuCombobox.getSelectedItem().toString();
    int quantity = Integer.parseInt(UOPQuantityCombobox.getSelectedItem().toString());
    double price = getPriceForMenu(menuName);
    double total = quantity * price;

    int orderID = orderIDCounter++;
    
    orderTableModel.addRow(new Object[]{orderID, menuName, quantity, price, total});
    
    calculateTotalPrice();
    }//GEN-LAST:event_UOPAddButtonMouseClicked

