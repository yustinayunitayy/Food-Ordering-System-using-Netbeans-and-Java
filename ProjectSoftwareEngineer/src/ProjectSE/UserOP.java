    private void UOPEditButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_UOPEditButtonMouseClicked
        int selectedRow = OrderTable.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(null, "Please select a row to edit.");
            return;
        }

        String menuName = UOPMenuCombobox.getSelectedItem().toString();
        int quantity = Integer.parseInt(UOPQuantityCombobox.getSelectedItem().toString());
        double price = getPriceForMenu(menuName);
        double total = quantity * price;

        OrderTable.setValueAt(menuName, selectedRow, 1);
        OrderTable.setValueAt(quantity, selectedRow, 2);
        OrderTable.setValueAt(price, selectedRow, 3);
        OrderTable.setValueAt(total, selectedRow, 4);

        JOptionPane.showMessageDialog(null, "Order Updated Successfully!");

    }//GEN-LAST:event_UOPEditButtonMouseClicked

