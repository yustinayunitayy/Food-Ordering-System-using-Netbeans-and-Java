     private void populateMenuComboBox() {
        try {
            String sql = "SELECT menu_name FROM menu";
            PreparedStatement statement = con.prepareStatement(sql);
            ResultSet rs = statement.executeQuery();
            menuModel = new DefaultComboBoxModel<>();
            while (rs.next()) {
                menuModel.addElement(rs.getString("menu_name"));
            }
            UOPMenuCombobox.setModel(menuModel);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error populating menu names: " + e.getMessage());
        }
    }
    
    private double getPriceForMenu(String menuName) {
        try {
            String sql = "SELECT price FROM menu WHERE menu_name=?";
            PreparedStatement statement = con.prepareStatement(sql);
            statement.setString(1, menuName);
            ResultSet rs = statement.executeQuery();
            if (rs.next()) {
                return rs.getDouble("price");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error getting price for menu: " + e.getMessage());
        }
        return 0.0;
    }
    
    private void calculateTotalPrice() {
        double totalPrice = 0.0;
        for (int i = 0; i < OrderTable.getRowCount(); i++) {
            Object value = OrderTable.getValueAt(i, 4);
            if (value != null) {
                double totalForRow = (double) value; // Assuming the total is in the 4th column
                totalPrice += totalForRow;
            }
        }
        jLabel3.setText(String.format("%.2f", totalPrice)); // Update the label with the total price
    }
    
