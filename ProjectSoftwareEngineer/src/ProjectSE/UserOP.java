    private void UOPOrderBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_UOPOrderBtnMouseClicked
    String name = OPNameTF.getText();
    String address = OPAddressTF.getText();
    String phoneNumber = OPPhoneTF.getText();
    String note = UOPNoteTF.getText();
    String paymentMethod = CashRB.isSelected() ? "Cash" : "TF"; 

    DefaultTableModel model = (DefaultTableModel) OrderTable.getModel();
    for (int i = 0; i < model.getRowCount(); i++) {
        String menuName = (String) model.getValueAt(i, 1);
        int quantity = (int) model.getValueAt(i, 2);
        double price = (double) model.getValueAt(i, 3);
        double total = (double) model.getValueAt(i, 4);

        try {
            String insertOrderQuery = "INSERT INTO orders (name, address, phone_number, note, payment_method, menu_name, quantity, price, total) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)";
            PreparedStatement insertOrderStatement = con.prepareStatement(insertOrderQuery);
            insertOrderStatement.setString(1, name);
            insertOrderStatement.setString(2, address);
            insertOrderStatement.setString(3, phoneNumber);
            insertOrderStatement.setString(4, note);
            insertOrderStatement.setString(5, paymentMethod);
            insertOrderStatement.setString(6, menuName);
            insertOrderStatement.setInt(7, quantity);
            insertOrderStatement.setDouble(8, price);
            insertOrderStatement.setDouble(9, total);
            insertOrderStatement.executeUpdate();
    } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error inserting order details: " + ex.getMessage());
        }
    }

    OPNameTF.setText("");
    OPAddressTF.setText("");
    OPPhoneTF.setText("");
    UOPNoteTF.setText("");
    CashRB.setSelected(true);
    UOPMenuCombobox.setSelectedIndex(0);
    UOPQuantityCombobox.setSelectedIndex(0);
    jLabel3.setText((""));
    model.setRowCount(0);

    JOptionPane.showMessageDialog(null, "Order placed successfully!");
    }//GEN-LAST:event_UOPOrderBtnMouseClicked
