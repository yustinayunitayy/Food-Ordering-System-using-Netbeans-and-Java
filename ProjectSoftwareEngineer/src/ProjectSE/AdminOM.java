    private void OMEditBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_OMEditBtnMouseClicked
    String OrderID = OMIDTF.getText();
    String Name = OMNameTF.getText();
    String Address = OMAddressTF.getText();
    String Phone = OMPhoneTF.getText();
    String Note = OMNoteTF.getText();
    String Payment = OMPaymentTF.getText();
    String MenuName = OMMenuNameTF.getText();
    int Quantity = Integer.parseInt(OMQuantityTF.getText());
    double Price = Double.parseDouble(OMPriceTF.getText());
    double Total = Double.parseDouble(OMTotalTF.getText());
    String DeliverStat = OMDeliveryTF.getText();

    try {
        String updateQuery = "UPDATE orders SET name=?, address=?, phone_number=?, note=?, payment_method=?, menu_name=?, quantity=?, price=?, total=?, delivery_status=? WHERE order_id=?";
        PreparedStatement updateStatement = con.prepareStatement(updateQuery);
        updateStatement.setString(1, Name);
        updateStatement.setString(2, Address);
        updateStatement.setString(3, Phone);
        updateStatement.setString(4, Note);
        updateStatement.setString(5, Payment);
        updateStatement.setString(6, MenuName);
        updateStatement.setInt(7, Quantity);
        updateStatement.setDouble(8, Price);
        updateStatement.setDouble(9, Total);
        updateStatement.setString(10, DeliverStat);
        updateStatement.setString(11, OrderID);
        updateStatement.executeUpdate();
        JOptionPane.showMessageDialog(null, "Order updated successfully!");
    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(null, "Invalid numerical input. Please enter valid numbers for quantity, price, and total.");
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(null, "Error updating order. Please try again!");
    }
    }//GEN-LAST:event_OMEditBtnMouseClicked

