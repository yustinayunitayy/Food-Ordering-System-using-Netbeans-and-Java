    public void table() {
        DefaultTableModel tbl = new DefaultTableModel();
        tbl.addColumn("Order ID");
        tbl.addColumn("Receiver Name");
        tbl.addColumn("Address");
        tbl.addColumn("Phone Number");
        tbl.addColumn("Note");
        tbl.addColumn("Payment Method");
        tbl.addColumn("Menu Name");
        tbl.addColumn("Quantity");
        tbl.addColumn("Price");
        tbl.addColumn("Total");
        tbl.addColumn("Delivery Status");

        try {
            Statement stat = con.createStatement();
            ResultSet rs = stat.executeQuery("SELECT * FROM orders");
        
            while (rs.next()) {
            tbl.addRow(new Object[] {
                rs.getString("order_id"),
                rs.getString("name"),
                rs.getString("address"),
                rs.getString("phone_number"),
                rs.getString("note"),
                rs.getString("payment_method"),
                rs.getString("menu_name"),
                rs.getString("quantity"),
                rs.getString("price"),
                rs.getString("total"),
                rs.getString("delivery_status")
                }); 
            }
            OrderTable.setModel(tbl);
        } catch (Exception e){
            JOptionPane.showMessageDialog (null, "Connection Error" + e.getMessage());
        }
    }
    
