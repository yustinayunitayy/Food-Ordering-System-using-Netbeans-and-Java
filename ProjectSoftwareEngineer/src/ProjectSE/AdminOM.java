    private void OMAddBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_OMAddBtnMouseClicked
        String OrderID = OMIDTF.getText();
        String Name = OMNameTF.getText();
        String Address = OMAddressTF.getText();
        String Phone = OMPhoneTF.getText();
        String Note = OMNoteTF.getText();
        String Payment = OMPaymentTF.getText();
        String MenuName = OMMenuNameTF.getText();
        int Quantity = Integer.parseInt(OMQuantityTF.getText());
        double Price = Double.parseDouble(OMPriceTF.getText());
        double Total = Double.parseDouble(OMTotalTF.getText());
        String DeliverStat = OMDeliveryTF.getText();

        try {
            if (OrderID.isEmpty() || Name.isEmpty() || Address.isEmpty() || Phone.isEmpty() || Note.isEmpty() || Payment.isEmpty() || MenuName.isEmpty() || Quantity <= 0 || Price <= 0 || Total <= 0 || DeliverStat.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Please fill in all fields and ensure numerical values are valid");
            } else {
                String insertOrderQuery = "INSERT INTO orders (name, address, phone_number, note, payment_method, menu_name, quantity, price, total) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)";
                PreparedStatement insertOrderStatement = con.prepareStatement(insertOrderQuery);
                insertOrderStatement.setString(1, Name);
                insertOrderStatement.setString(2, Address);
                insertOrderStatement.setString(3, Phone);
                insertOrderStatement.setString(4, Note);
                insertOrderStatement.setString(5, Payment);
                insertOrderStatement.setString(6, MenuName);
                insertOrderStatement.setInt(7, Quantity);
                insertOrderStatement.setDouble(8, Price);
                insertOrderStatement.setDouble(9, Total);
                insertOrderStatement.executeUpdate();
                JOptionPane.showMessageDialog(null, "Order successfully inserted!");
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Invalid numerical input. Please enter valid numbers for quantity, price, and total.");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error inserting order. Please try again!");
        }
    }//GEN-LAST:event_OMAddBtnMouseClicked

